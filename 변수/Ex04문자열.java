public class Ex04문자열 {

	public static void main(String[] args) {

		// 문자열 Hello World!
		// String : reference type, "" - 대문자로 시작
		// 값 저장하는 것 외에 기능이 있다
		String s = "Hello World!";

		// char : primitive type(기본타입), '' - 소문자로 시작
		// 값 저장하는 것 외에 기능이 없다
		char c = 'c';

		System.out.println(s);

	}

}
